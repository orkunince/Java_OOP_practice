package kodlamaIo1;

public class Course {

	int id;
	String name;
	String instructor;
	String resimUrl;

	public Course() {

	}

	public Course(int id, String name, String instructor, String resimUrl) {
		this.id = id;
		this.name = name;
		this.instructor = instructor;
		this.resimUrl = resimUrl;
	}

}
